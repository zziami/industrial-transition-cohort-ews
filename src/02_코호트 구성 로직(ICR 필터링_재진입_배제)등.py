


# 코호트위경도 파일에 시도명 y(한계기업여부)_산단_최종분류 1:1 매칭
# 지도에 점 찍을때 필요한 정보
'''
회색 → 코호트 미포함 전체 공장 (~208,000개)
노란색 → 코호트 Y=0 (ICR<1 진입했다가 3년 연속 안 된 기업, 8,496개)
빨간색 → 코호트 Y=1 (ICR<1 3년 연속 → 한계기업 확정, 3,379개)

t년도에 ICR<1 최초 진입
t+1년도 ICR<1 지속
t+2년도 ICR<1 지속 → Y=1 확정
3년 연속 ICR<1 이면 Y=1, 중간에 회복하면 Y=0.
한국은행/KDB 공식 한계기업 정의 기준이야.
'''

# 지도용 데이터 3개 레이어 만들기
# Layer 1 (회색) — 공장 위경도 전체에서 코호트 제외한 것
# Layer 2 (노란색) — 코호트 Y=0
# Layer 3 (빨간색) — 코호트 Y=1

코호트_필요컬럼 = 코호트[['거래소코드', '시도명', 'Y', '산단_최종분류']]
df = 위경도.merge(코호트_필요컬럼, on='거래소코드', how='left')

print(df.shape)
print(f"null 체크 - 시도명: {df['시도명'].isna().sum()}, Y: {df['Y'].isna().sum()}")
