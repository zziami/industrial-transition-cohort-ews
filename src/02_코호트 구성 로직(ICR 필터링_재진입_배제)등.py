# ============================================================
# Target Labeling (Y 생성)
# ICR<1 최초 진입 코호트 추출 → 3년 연속 ICR<1 → Y=1
# ============================================================

df = df.sort_values(['거래소코드', '회계년도']).reset_index(drop=True)
# 데이터 정렬 - shift 연산기준점 확보 (기업별 연도별 정렬 필수)
g = df.groupby('거래소코드')

# Step 1. ICR 계산 - 영업외 비용 이자비용으로 계산
# 이자보상배율 산출 분모인 이자비용이 0이면 무한대 오류방지를 위하여 nan처리
df['ICR'] = df['[U01230000][제조]영업이익(손실)(천원)'] / \
            df['[U01250010][제조]   이자비용(천원)'].replace(0, np.nan)

# Step 2. ICR<1 여부 플래그 (이자비용 0 이하면 정상으로 처리)
# 이자비용이 없으면 0(정상)으로 간주, icr < 1인 경우만 1로 표시
df['ICR_미만1'] = np.where(
    df['[U01250010][제조]   이자비용(천원)'] <= 0, 0,
    np.where(df['ICR'] < 1, 1, 0)
)

# Step 3. 전년도 ICR<1 여부 (최초 진입 확인용)
# 한계기업 진입 여부 판단하기 위해 전년도 상태 확인 - shift(1)로 전기 데이터 가져오기
df['ICR_미만1_전기'] = g['ICR_미만1'].shift(1)

# Step 4. 최초 진입 여부: 2010년부터 전기에 한계기업이 아니었던(NaN 포함) 기업의 첫 진입시점 확인
df['최초진입'] = 0
mask = (df['회계년도'] >= 2010) & (df['ICR_미만1'] == 1) & (df['ICR_미만1_전기'] != 1)
df.loc[mask, '최초진입'] = 1

# Step 5. t+1, t+2 ICR<1 여부 - 미래시점 확보, 진입 후 1년/2년 경과 시점의 상태를 미리 가져옴
df['ICR_미만1_t1'] = g['ICR_미만1'].shift(-1)
df['ICR_미만1_t2'] = g['ICR_미만1'].shift(-2)
df['회계년도_t1']  = g['회계년도'].shift(-1)
df['회계년도_t2']  = g['회계년도'].shift(-2)

# Step 6. 연도 연속성 체크 (t+1이 실제 t+1년도, t+2가 실제 t+2년도인지)
# 데이터 누락으로 시계열이 끊기는 경우를 배제하기 위한 연도차 연산
df['연속_t1'] = (df['회계년도_t1'] - df['회계년도']) == 1
df['연속_t2'] = (df['회계년도_t2'] - df['회계년도']) == 2

# Step 7. Y 생성 (최초 진입 기업 중 t~t+2 3년 연속 ICR<1 → Y=1)
# 최초 진입이면서 t+1, t+2도 ICR<1이고 연도도 연속인 경우 Y=1
# 최초 진입이지만 그렇지 않은 경우 Y=0, 최초 진입이 아닌 경우 Y=NaN
df['Y'] = np.nan

최초진입_mask = df['최초진입'] == 1
연속_mask = df['연속_t1'] & df['연속_t2']
icr3년_mask = (df['ICR_미만1'] == 1) & (df['ICR_미만1_t1'] == 1) & (df['ICR_미만1_t2'] == 1)

# Y=1: 최초진입 + 연도연속 + 3년연속 ICR<1 (연속성 충족)
df.loc[최초진입_mask & 연속_mask & icr3년_mask, 'Y'] = 1

# Y=0: 최초진입이지만 위 조건 미충족 (정상/회복군)
df.loc[최초진입_mask & ~(연속_mask & icr3년_mask), 'Y'] = 0



# ============================================================
# 재진입 배제 로직 (재진입 기업 5,054개 발견 → 기업당 첫 진입만 유지)
# ============================================================

# 첫 진입만 남겼을 때 Y 분포
'''t   : ICR<1 첫 진입 (최초진입=1인 행)
t+1 : ICR<1 여부
t+2 : ICR<1 여부
→ t, t+1, t+2 모두 ICR<1 이면 Y=1'''

첫진입_idx = df[df['최초진입']==1].groupby('거래소코드')['회계년도'].idxmin()
df_첫진입 = df.loc[첫진입_idx]

print(f"첫진입 기업 수 : {len(df_첫진입):,}")
print(f"Y=1 기업 수   : {(df_첫진입['Y']==1).sum():,}")
print(f"Y=0 기업 수   : {(df_첫진입['Y']==0).sum():,}")
print(f"Y=1 비율      : {(df_첫진입['Y']==1).mean()*100:.1f}%")



# ============================================================
# 재진입 배제 판단 근거 (재진입 시 Train/Test 데이터 누수 발생 확인)
# ============================================================
train_기업 = df[(df['최초진입']==1) & (df['회계년도'] <= 2019)]['거래소코드'].unique()
test_기업  = df[(df['최초진입']==1) & (df['회계년도'].isin([2020,2021,2022]))]['거래소코드'].unique()
oos_기업   = df[(df['최초진입']==1) & (df['회계년도'] == 2023)]['거래소코드'].unique()

train_test_걸침 = set(train_기업) & set(test_기업)
train_oos_걸침  = set(train_기업) & set(oos_기업)
test_oos_걸침   = set(test_기업)  & set(oos_기업)

print(f"Train/Test 양쪽 걸친 기업 수 : {len(train_test_걸침):,}")
print(f"Train/OOS  양쪽 걸친 기업 수 : {len(train_oos_걸침):,}")
print(f"Test/OOS   양쪽 걸친 기업 수 : {len(test_oos_걸침):,}")
